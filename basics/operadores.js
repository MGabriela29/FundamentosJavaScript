


// Operadores Matemáticos
// + - * / % **
// += += *= /= %=
console.log(10 + 10);
console.log(10 - 10);
console.log(10 / 10);
console.log(10 * 10);
console.log(10 % 10);
console.log(10 ** 10); //potencia

let num = 15;
console.log(num += 5); // num = num +5
console.log(num -= 5);
console.log(num *= 5);
console.log(num /= 5);
console.log(num %= 5);

// Operadores booleanos
// > < >= <= !== ===
console.log(1 > 2);
console.log(1 < 2);
console.log(1 >= 2);
console.log(1 <= 2);
console.log(1 !== 2);
console.log(1 === 2);

// Operadores Lógicos
// and &&
console.log(true && true);
// or ||
console.log(true || true);
// not !
console.log(!true);